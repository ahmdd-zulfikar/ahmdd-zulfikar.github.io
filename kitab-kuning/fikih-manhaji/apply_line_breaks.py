import json

with open('D:/Project/ahmdd-zulfikar.github.io/kitab-kuning/fikih-manhaji/data.js', 'r', encoding='utf-8') as f:
    content = f.read()

json_str = content[content.find('['):content.rfind(';')]
data = json.loads(json_str)

def find_word_index(words, search_text):
    for i, w in enumerate(words):
        if search_text in w['text']:
            return i
    return -1

# We will define line breaks as the exact index of the word in each block.
# We will do this manually for the pages based on our transcriptions.

# Function to assign breaks by indices
def assign_breaks(page_idx, block_idx, indices):
    if page_idx < len(data) and block_idx < len(data[page_idx]['blocks']):
        words = data[page_idx]['blocks'][block_idx].get('words', [])
        for i in indices:
            if i < len(words):
                words[i]['br'] = True

# First, reset all breaks
for p in data:
    for b in p['blocks']:
        if 'words' in b:
            for w in b['words']:
                w['br'] = False

